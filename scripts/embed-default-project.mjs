// Embeds default-dashboard/ into src/studio/defaultProject.generated.ts for the HACS bundle.

import { writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { readDefaultProject } from './project-files.mjs';

const here = dirname(fileURLToPath(import.meta.url));
const root = join(here, '..');
const outPath = join(root, 'src/studio/defaultProject.generated.ts');

const project = readDefaultProject();
if (!project) {
  throw new Error('default-dashboard/ is empty or missing.');
}

const filesJson = JSON.stringify(project.files, null, 2);
const body = `/** AUTO-GENERATED by scripts/embed-default-project.mjs — do not edit. Source: default-dashboard/ */
import type { Project } from './project';

export const DEFAULT_PROJECT: Project = {
  entry: ${JSON.stringify(project.entry)},
  files: ${filesJson},
};
`;

writeFileSync(outPath, body);
console.log(`✓ embedded default-dashboard/ → ${outPath.replace(/\\/g, '/')} (${Object.keys(project.files).length} files)`);
