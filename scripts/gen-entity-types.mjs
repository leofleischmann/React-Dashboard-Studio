// Connects to Home Assistant and writes entity IDs for autocomplete + dashboard/.
// CLI: npm run gen:types
// Also called automatically after npm run sync:pull

import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import {
  createConnection,
  createLongLivedTokenAuth,
  getStates,
} from 'home-assistant-js-websocket';
import WebSocket from 'ws';
import { DASHBOARD_DIR } from './dashboard-local.mjs';

globalThis.WebSocket = WebSocket;

const defaultRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');

function loadEnv(root = defaultRoot) {
  const env = { ...process.env };
  const envPath = path.join(root, '.env.local');
  if (existsSync(envPath)) {
    for (const line of readFileSync(envPath, 'utf8').split('\n')) {
      const m = line.match(/^\s*([\w.-]+)\s*=\s*(.*)\s*$/);
      if (m && !line.trim().startsWith('#')) {
        env[m[1]] = m[2].replace(/^["']|["']$/g, '').trim();
      }
    }
  }
  return env;
}

function writeEntityTypeFiles(root, states) {
  const ids = states.map((s) => s.entity_id).sort();
  const union = ids.map((id) => `  | '${id}'`).join('\n');
  const countLine = `// ${ids.length} entities`;

  mkdirSync(DASHBOARD_DIR, { recursive: true });

  const dtsOut =
    `// AUTO-GENERATED by \`npm run gen:types\`. Do not edit by hand.\n` +
    `${countLine}\n` +
    `declare type KnownEntityId =\n${union}\n  | (string & {});\n`;

  writeFileSync(path.join(DASHBOARD_DIR, 'ha-entities.d.ts'), dtsOut);
  console.log(`✓ ${ids.length} Entities → dashboard/ha-entities.d.ts`);

  const tsconfigOut = `{
  "compilerOptions": {
    "target": "ES2020",
    "lib": ["ES2020", "DOM"],
    "module": "ESNext",
    "moduleResolution": "bundler",
    "jsx": "react-jsx",
    "noEmit": true,
    "strict": false,
    "skipLibCheck": true,
    "paths": {
      "@ha": ["../src/sdk/hass/hooks.ts"],
      "@ha/ui": ["../src/sdk/ui/index.ts"],
      "@ha/layout": ["../src/sdk/ui/layout.tsx"],
      "@ha/format": ["../src/sdk/format.ts"]
    }
  },
  "include": ["./**/*.ts", "./**/*.tsx", "ha-entities.d.ts"]
}
`;
  writeFileSync(path.join(DASHBOARD_DIR, 'tsconfig.json'), tsconfigOut);
  console.log('✓ VS Code Typen → dashboard/tsconfig.json');

  const byDomain = new Map();
  for (const entity of states) {
    const domain = entity.entity_id.split('.')[0];
    if (!byDomain.has(domain)) byDomain.set(domain, []);
    byDomain.get(domain).push(entity);
  }
  for (const list of byDomain.values()) {
    list.sort((a, b) => a.entity_id.localeCompare(b.entity_id));
  }

  const mdLines = [
    `# Home Assistant Entities (${ids.length})`,
    '',
    `> Automatisch generiert von \`npm run gen:types\` · ${ids.length} Entities`,
    `> In VS Code durchsuchbar — oder im HA-Studio über **⚡ Sensor / Aktion**.`,
    '',
  ];
  for (const domain of [...byDomain.keys()].sort()) {
    const list = byDomain.get(domain);
    mdLines.push(`## ${domain} (${list.length})`, '');
    mdLines.push('| Entity ID | Name | Zustand |');
    mdLines.push('| --- | --- | --- |');
    for (const e of list) {
      const name = (e.attributes.friendly_name ?? '').replace(/\|/g, '\\|');
      const unit = e.attributes.unit_of_measurement;
      const state = `${e.state}${unit ? ` ${unit}` : ''}`.replace(/\|/g, '\\|');
      mdLines.push(`| \`${e.entity_id}\` | ${name} | ${state} |`);
    }
    mdLines.push('');
  }

  writeFileSync(path.join(DASHBOARD_DIR, 'ENTITIES.md'), mdLines.join('\n'));
  console.log(`✓ Entity-Referenz → dashboard/ENTITIES.md (${byDomain.size} Domains)`);

  return ids.length;
}

/** Fetch entities from HA and write type/reference files. */
export async function generateEntityTypes(options = {}) {
  const root = options.root ?? defaultRoot;
  const env = loadEnv(root);
  const hassUrl = (options.hassUrl ?? env.VITE_HASS_URL ?? process.env.VITE_HASS_URL)?.replace(
    /\/+$/,
    '',
  );
  const token = options.token ?? env.VITE_HASS_TOKEN ?? process.env.VITE_HASS_TOKEN;

  if (!hassUrl || !token) {
    throw new Error('Bitte VITE_HASS_URL und VITE_HASS_TOKEN in .env.local setzen.');
  }

  const auth = createLongLivedTokenAuth(hassUrl, token);
  const connection = await createConnection({ auth });
  try {
    const states = await getStates(connection);
    return writeEntityTypeFiles(root, states);
  } finally {
    connection.close();
  }
}

const isMain =
  process.argv[1] &&
  path.resolve(process.argv[1]) === path.resolve(fileURLToPath(import.meta.url));

if (isMain) {
  try {
    await generateEntityTypes();
  } catch (err) {
    console.error(err.message);
    process.exit(1);
  }
}
